package xft.workbench.backstage.warn.model;


public class MaterialWarn {
	private  Integer id;
	private  Integer jy_material_id;
	private  String warn_storehouse_code;
	private  Integer warn_number;
	private  String remark;
	private  Integer inputuser;
	private  String crt_date;
	private  String crt_time;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getJy_material_id() {
		return jy_material_id;
	}
	public void setJy_material_id(Integer jy_material_id) {
		this.jy_material_id = jy_material_id;
	}
	public String getWarn_storehouse_code() {
		return warn_storehouse_code;
	}
	public void setWarn_storehouse_code(String warn_storehouse_code) {
		this.warn_storehouse_code = warn_storehouse_code;
	}
	public Integer getWarn_number() {
		return warn_number;
	}
	public void setWarn_number(Integer warn_number) {
		this.warn_number = warn_number;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public Integer getInputuser() {
		return inputuser;
	}
	public void setInputuser(Integer inputuser) {
		this.inputuser = inputuser;
	}
	public String getCrt_date() {
		return crt_date;
	}
	public void setCrt_date(String crt_date) {
		this.crt_date = crt_date;
	}
	public String getCrt_time() {
		return crt_time;
	}
	public void setCrt_time(String crt_time) {
		this.crt_time = crt_time;
	}
	
}
